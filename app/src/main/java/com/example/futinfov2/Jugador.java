package com.example.futinfov2;

public class Jugador {
    String nombre;
    String posicion;
    int media;
    String pais;
    String equipo;
    String cara;
    String tipo;

    public Jugador(String nombre, String posicion, int media, String pais, String equipo, String cara, String tipo) {
        this.nombre = nombre;
        this.posicion = posicion;
        this.media = media;
        this.pais = pais;
        this.equipo = equipo;
        this.cara = cara;
        this.tipo = tipo;
    }
}
